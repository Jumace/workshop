import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const contentRoot = path.join(projectRoot, "content");
const outputPath = path.join(projectRoot, "app", "lib", "content.generated.ts");

function getEntries(collection) {
  const collectionRoot = path.join(contentRoot, collection);

  if (!fs.existsSync(collectionRoot)) {
    return [];
  }

  return fs
    .readdirSync(collectionRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .filter((entry) => fs.existsSync(path.join(collectionRoot, entry.name, "index.mdx")))
    .map((entry, index) => {
      const filePath = path.join(collectionRoot, entry.name, "index.mdx");
      return {
        slug: entry.name,
        source: fs.readFileSync(filePath, "utf8"),
        componentName: `${collection}${index}Component`,
        metadataName: `${collection}${index}Metadata`,
        importPath: `@/content/${collection}/${entry.name}/index.mdx`,
      };
    })
    .sort((a, b) => a.slug.localeCompare(b.slug));
}

const blogEntries = getEntries("blog");
const labEntries = getEntries("lab");

function renderImports(entries) {
  return entries
    .map(
      (entry) =>
        `import ${entry.componentName}, { metadata as ${entry.metadataName} } from ${JSON.stringify(
          entry.importPath,
        )};`,
    )
    .join("\n");
}

function renderEntry(entry, metadataType) {
  return `  {
    slug: ${JSON.stringify(entry.slug)},
    source: ${JSON.stringify(entry.source)},
    module: {
      default: ${entry.componentName},
      metadata: ${entry.metadataName} as ${metadataType},
    },
  }`;
}

const content = `// This file is generated by scripts/generate-content-registry.mjs.
// Do not edit it directly.
import type { BlogMetadata, LabMetadata } from "./content";

${renderImports([...blogEntries, ...labEntries])}

export const blogContent = [
${blogEntries.map((entry) => renderEntry(entry, "BlogMetadata")).join(",\n")}
];

export const labContent = [
${labEntries.map((entry) => renderEntry(entry, "LabMetadata")).join(",\n")}
];
`;

fs.writeFileSync(outputPath, content);
