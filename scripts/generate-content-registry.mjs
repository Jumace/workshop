import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const contentRoot = path.join(projectRoot, "content");
const outputPath = path.join(projectRoot, "app", "lib", "content.generated.ts");
const metadataOutputPath = path.join(projectRoot, "lib", "content-metadata.generated.ts");

function extractMetadata(source, file) {
  const marker = "export const metadata =";
  const start = source.indexOf(marker);

  if (start === -1) {
    throw new Error(`${file} is missing exported metadata.`);
  }

  const objectStart = source.indexOf("{", start);
  let depth = 0;

  for (let index = objectStart; index < source.length; index += 1) {
    const character = source[index];

    if (character === "{") {
      depth += 1;
    }

    if (character === "}") {
      depth -= 1;

      if (depth === 0) {
        const objectSource = source.slice(objectStart, index + 1);

        return Function(`"use strict"; return (${objectSource});`)();
      }
    }
  }

  throw new Error(`${file} has an unterminated metadata object.`);
}

function getEntries(collection) {
  const collectionRoot = path.join(contentRoot, collection);

  if (!fs.existsSync(collectionRoot)) {
    return [];
  }

  return fs
    .readdirSync(collectionRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .filter((entry) => fs.existsSync(path.join(collectionRoot, entry.name, "index.mdx")))
    .map((entry, index) => {
      const filePath = path.join(collectionRoot, entry.name, "index.mdx");
      const sourcePath = `content/${collection}/${entry.name}/index.mdx`;
      const source = fs.readFileSync(filePath, "utf8");

      return {
        slug: entry.name,
        source,
        sourcePath,
        metadata: extractMetadata(source, sourcePath),
        componentName: `${collection}${index}Component`,
        metadataName: `${collection}${index}Metadata`,
        importPath: `@/content/${collection}/${entry.name}/index.mdx`,
      };
    })
    .sort((a, b) => a.slug.localeCompare(b.slug));
}

const blogEntries = getEntries("blog");
const labEntries = getEntries("lab");

function renderImports(entries) {
  return entries
    .map(
      (entry) =>
        `import ${entry.componentName}, { metadata as ${entry.metadataName} } from ${JSON.stringify(
          entry.importPath,
        )};`,
    )
    .join("\n");
}

function renderEntry(entry, metadataType) {
  return `  {
    slug: ${JSON.stringify(entry.slug)},
    source: ${JSON.stringify(entry.source)},
    module: {
      default: ${entry.componentName},
      metadata: ${entry.metadataName} as ${metadataType},
    },
  }`;
}

const content = `// This file is generated by scripts/generate-content-registry.mjs.
// Do not edit it directly.
import type { BlogMetadata, LabMetadata } from "./content";

${renderImports([...blogEntries, ...labEntries])}

export const blogContent = [
${blogEntries.map((entry) => renderEntry(entry, "BlogMetadata")).join(",\n")}
];

export const labContent = [
${labEntries.map((entry) => renderEntry(entry, "LabMetadata")).join(",\n")}
];
`;

fs.writeFileSync(outputPath, content);

function renderMetadataEntries(type, entries) {
  return entries
    .map(
      (entry) => `  {
    type: ${JSON.stringify(type)},
    slug: ${JSON.stringify(entry.slug)},
    href: ${JSON.stringify(`/${type}/${entry.slug}`)},
    sourcePath: ${JSON.stringify(entry.sourcePath)},
    metadata: ${JSON.stringify(entry.metadata)},
  }`,
    )
    .join(",\n");
}

const metadataContent = `// This file is generated by scripts/generate-content-registry.mjs.
// Do not edit it directly.
import type { ContentReference } from "./content-metadata";

export const contentReferences = [
${renderMetadataEntries("blog", blogEntries)},
${renderMetadataEntries("lab", labEntries)}
] satisfies ContentReference[];
`;

fs.writeFileSync(metadataOutputPath, metadataContent);
